#include<iostream>
using namespace std;

int main(){
    float angka1, angka2;
    cout << "masukkan angka pertama : ";
    cin >> angka1;
    cout << "masukkan angka kedua : ";
    cin >> angka2;

    cout << "penjumlahan : " << angka1 + angka2 << endl;
    cout << "pengurangan : " << angka1 - angka2 << endl;
    cout << "perkalian : " << angka1 * angka2 << endl;
    cout << "pembagian : " << angka1 / angka2 << endl;

    return 0;
}